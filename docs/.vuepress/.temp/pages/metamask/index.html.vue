<template><h1 id="metamask"><a class="header-anchor" href="#metamask">#</a> MetaMask</h1>
<p>In order to bet BNB on Token Race, you need to install MetaMask -- the browser-based blockchain wallet.</p>
<h2 id="_1-install-metamask"><a class="header-anchor" href="#_1-install-metamask">#</a> 1 - Install MetaMask</h2>
<ol>
<li><a href="https://metamask.io/download.html" target="_blank" rel="noopener noreferrer">Install MetaMask<OutboundLink/></a> for your chosen browser.</li>
<li>Follow the instructions to create your first wallet. Make sure to save the key in a safe &amp; secure place!</li>
</ol>
<h2 id="_2-add-the-binance-smart-chain-network"><a class="header-anchor" href="#_2-add-the-binance-smart-chain-network">#</a> 2 - Add the Binance Smart Chain Network</h2>
<p>MetaMask defaults to using the Ethereum blockchain, so we have to add the Binance Smart Chain blockchain.</p>
<ol>
<li><strong>Click the MetaMask icon</strong> in your extensions bar to open it</li>
<li>Click the round button at the top that says 'Ethereum Mainnet'</li>
</ol>
<img src="/images/metamask1.png" width="320"/>
<ol start="3">
<li>Select <strong>'Custom RPC'</strong> at the bottom</li>
<li>Enter these values</li>
</ol>
<ul>
<li>Network Name: <strong>Binance Smart Chain</strong></li>
<li>New RPC URL: <strong>https://bsc-dataseed.binance.org/</strong></li>
<li>ChainID: <strong>56</strong></li>
<li>Symbol: <strong>BNB</strong></li>
<li>Block Explorer URL: <strong>https://bscscan.com</strong></li>
</ul>
<ol start="5">
<li>Click <strong>'Save'</strong></li>
</ol>
<h2 id="_3-send-bnb-to-your-wallet"><a class="header-anchor" href="#_3-send-bnb-to-your-wallet">#</a> 3 - Send BNB to your wallet</h2>
<p>Now that your wallet is set up, you need some BNB to play with!</p>
<ol>
<li><strong>Buy BNB</strong> on your preferred exchange</li>
<li><strong>Send it to your wallet address</strong> using the Binance Smart Chain network (NOT Ethereum)</li>
</ol>
<p><strong>Note:</strong> If you already have BNB on the Ethereum blockchain, or your exchange doesn't support withdrawing using the Binance Smart Chain, you can use the <a href="https://www.binance.org/en/bridge" target="_blank" rel="noopener noreferrer">Binance Bridge<OutboundLink/></a> to move it over.</p>
<h2 id="_4-deposit-bnb-into-token-race"><a class="header-anchor" href="#_4-deposit-bnb-into-token-race">#</a> 4 - Deposit BNB into Token Race</h2>
<p>The final step is to deposit BNB into your Token Race account so you can use it for betting.</p>
<ol>
<li><strong>Click the 'Connect Wallet' button</strong> in the top-right of the Token Race website</li>
<li>Make sure the Binance Smart Chain network is selected, as well as the wallet that contains your BNB</li>
<li><strong>Click the 'Deposit' button</strong> in the resulting modal and select how much BNB you want to be able to wager with.</li>
</ol>
